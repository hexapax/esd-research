#!/usr/bin/env python3
"""Extract all ESDPA data quality issues from the esd-dataset/ YAML frontmatter.

Generates a summary report of date corrections, location errors, misclassifications,
and other data quality findings. Can output as markdown or CSV.

Usage:
    python3 extract_issues.py              # Print markdown to stdout
    python3 extract_issues.py --csv        # Print CSV to stdout
    python3 extract_issues.py --out FILE   # Write markdown to file
"""

import yaml
import re
import glob
import os
import sys
import csv
from io import StringIO

DATASET_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)))


def parse_yaml_frontmatter(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    match = re.match(r'^---\s*\n(.*?)\n---', content, re.DOTALL)
    if not match:
        return None
    try:
        return yaml.safe_load(match.group(1))
    except yaml.YAMLError:
        return None


def extract_all_issues():
    files = sorted(glob.glob(os.path.join(DATASET_DIR, "ESD-*.md")))
    results = {
        'date_corrections': [],
        'location_corrections': [],
        'misclassifications': [],
        'phantom_duplicates': [],
        'other_issues': [],
        'all_issues': [],
    }

    for fp in files:
        d = parse_yaml_frontmatter(fp)
        if not d:
            continue

        iid = d.get('incident_id', os.path.basename(fp))
        date = d.get('date', '')
        state = d.get('state', '')
        verification = d.get('verification_level', '')
        esdpa_listed = d.get('esdpa_listed', False)
        date_correct = d.get('esdpa_date_correct')
        esdpa_date_listed = d.get('esdpa_date_listed', '')
        issues = d.get('esdpa_data_issues', [])
        refs = d.get('project_refs', [])

        if not esdpa_listed:
            continue  # Only report issues for ESDPA-listed incidents

        # Date corrections
        if date_correct is False and esdpa_date_listed:
            results['date_corrections'].append({
                'incident_id': iid,
                'esdpa_date': esdpa_date_listed,
                'corrected_date': date,
                'state': state,
                'refs': ', '.join(refs) if isinstance(refs, list) else str(refs),
            })

        # Categorize issues
        if issues and isinstance(issues, list):
            for issue in issues:
                if not isinstance(issue, str):
                    continue

                entry = {
                    'incident_id': iid,
                    'date': date,
                    'state': state,
                    'issue': issue,
                    'verification': verification,
                }
                results['all_issues'].append(entry)

                issue_lower = issue.lower()
                if 'phantom' in issue_lower or 'duplicate' in issue_lower:
                    results['phantom_duplicates'].append(entry)
                elif any(w in issue_lower for w in ['location', 'state', 'city', 'county', 'lake name', 'spelling']):
                    results['location_corrections'].append(entry)
                elif any(w in issue_lower for w in ['date', 'year', 'month']):
                    results['date_corrections_detail'] = results.get('date_corrections_detail', [])
                    results['date_corrections_detail'].append(entry)
                elif any(w in issue_lower for w in ['classified', 'fatality', 'near-miss', 'survived', 'died']):
                    results['misclassifications'].append(entry)
                else:
                    results['other_issues'].append(entry)

    return results


def format_markdown(results):
    lines = []
    lines.append("# ESDPA Data Quality Issues — Extracted from esd-dataset/")
    lines.append("")
    lines.append(f"*Auto-generated by `extract_issues.py` from {len(glob.glob(os.path.join(DATASET_DIR, 'ESD-*.md')))} dataset files.*")
    lines.append("")
    lines.append(f"**Total issues found:** {len(results['all_issues'])}")
    lines.append("")

    # Date corrections
    dc = results['date_corrections']
    lines.append(f"## Date Corrections ({len(dc)})")
    lines.append("")
    if dc:
        lines.append("| Incident ID | ESDPA Date | Corrected Date | State |")
        lines.append("|-------------|-----------|----------------|-------|")
        for r in sorted(dc, key=lambda x: x.get('corrected_date', '')):
            lines.append(f"| {r['incident_id']} | {r['esdpa_date']} | {r['corrected_date']} | {r['state']} |")
        lines.append("")

    # Location corrections
    lc = results['location_corrections']
    lines.append(f"## Location Issues ({len(lc)})")
    lines.append("")
    if lc:
        lines.append("| Incident ID | Date | State | Issue |")
        lines.append("|-------------|------|-------|-------|")
        for r in sorted(lc, key=lambda x: str(x.get('date', '') or '')):
            lines.append(f"| {r['incident_id']} | {r['date']} | {r['state']} | {r['issue']} |")
        lines.append("")

    # Misclassifications
    mc = results['misclassifications']
    lines.append(f"## Misclassifications ({len(mc)})")
    lines.append("")
    if mc:
        lines.append("| Incident ID | Date | State | Issue |")
        lines.append("|-------------|------|-------|-------|")
        for r in sorted(mc, key=lambda x: str(x.get('date', '') or '')):
            lines.append(f"| {r['incident_id']} | {r['date']} | {r['state']} | {r['issue']} |")
        lines.append("")

    # Phantom duplicates
    pd = results['phantom_duplicates']
    lines.append(f"## Phantom Duplicates ({len(pd)})")
    lines.append("")
    if pd:
        for r in pd:
            lines.append(f"- **{r['incident_id']}** ({r['date']}, {r['state']}): {r['issue']}")
        lines.append("")

    # Other
    oi = results['other_issues']
    lines.append(f"## Other Issues ({len(oi)})")
    lines.append("")
    if oi:
        lines.append("| Incident ID | Date | State | Issue |")
        lines.append("|-------------|------|-------|-------|")
        for r in sorted(oi, key=lambda x: str(x.get('date', '') or '')):
            issue_trunc = r['issue'][:100] + '...' if len(r['issue']) > 100 else r['issue']
            lines.append(f"| {r['incident_id']} | {r['date']} | {r['state']} | {issue_trunc} |")
        lines.append("")

    return "\n".join(lines)


def format_csv(results):
    buf = StringIO()
    writer = csv.DictWriter(buf, fieldnames=['incident_id', 'date', 'state', 'verification', 'issue'])
    writer.writeheader()
    for r in results['all_issues']:
        writer.writerow(r)
    return buf.getvalue()


def main():
    results = extract_all_issues()

    if '--csv' in sys.argv:
        print(format_csv(results))
    else:
        md = format_markdown(results)
        if '--out' in sys.argv:
            idx = sys.argv.index('--out')
            if idx + 1 < len(sys.argv):
                with open(sys.argv[idx + 1], 'w') as f:
                    f.write(md)
                print(f"Wrote {sys.argv[idx + 1]}")
            else:
                print("Error: --out requires a filename", file=sys.stderr)
                sys.exit(1)
        else:
            print(md)


if __name__ == "__main__":
    main()
